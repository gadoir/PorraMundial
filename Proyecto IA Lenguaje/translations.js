function changeLanguage(lang) {
    localStorage.setItem('selected_lang', lang);
    applyTranslations(lang);
    updateLangDropdown(lang);
    
    // Feedback visual en botones (si existen los antiguos)
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.dataset.lang === lang) {
            btn.classList.add('ring-2', 'ring-brand-green', 'bg-brand-green/20');
        } else {
            btn.classList.remove('ring-2', 'ring-brand-green', 'bg-brand-green/20');
        }
    });
}

function updateLangDropdown(lang) {
    const langText = document.getElementById('current-lang-text');
    if (!langText) return;

    const texts = { es: 'Español', va: 'Valencià', en: 'English' };
    langText.innerText = texts[lang] || texts.es;
}

function applyTranslations(lang) {
    const dict = translations[lang] || translations.es;
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (dict[key]) {
            let text = dict[key];
            
            // Reemplazo de variables dinámicas
            if (key === 'user_welcome') {
                const username = localStorage.getItem('logged_user') || 'Usuario';
                text = text.replace('{name}', username);
            }
            if (key === 'inscripcion_plazas_libres') {
                const leftElement = document.getElementById('inscription-left');
                let n = '25';
                if (leftElement) {
                    const match = leftElement.innerText.match(/\d+/);
                    if (match) n = match[0];
                }
                text = text.replace('{n}', n);
            }
            
            if (el.tagName === 'INPUT' && (el.type === 'button' || el.type === 'submit')) {
                el.value = text;
            } else {
                el.innerText = text;
            }
        }
    });

    // Cambiar el atributo lang del HTML
    document.documentElement.lang = lang;
}

function t(key) {
    const lang = localStorage.getItem('selected_lang') || 'es';
    return (translations[lang] && translations[lang][key]) || (translations.es[key]) || key;
}

// Inicializar al cargar
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selected_lang') || 'es';
    applyTranslations(savedLang);
    updateLangDropdown(savedLang);
    
    // Resaltar botón activo
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.dataset.lang === savedLang) {
            btn.classList.add('ring-2', 'ring-brand-green', 'bg-brand-green/20');
        }
    });
});
